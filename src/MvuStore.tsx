// ─────────────────────────────────────────────
// MVU reader — reads stat_data from the current message floor
// ─────────────────────────────────────────────

const readMvuData = (): MvuData | null => {
  const w = window as any;
  if (!w.Mvu || !w.getCurrentMessageId || !w._) return null;
  try {
    const variables = w.Mvu.getMvuData({ type: 'message', message_id: w.getCurrentMessageId() });
    const stat_data = w._.get(variables, 'stat_data');
    if (!stat_data || !stat_data.全局信息 || !stat_data.结缘) return null;
    return stat_data as MvuData;
  } catch {
    return null;
  }
};

// ─────────────────────────────────────────────
// React context + provider
// ─────────────────────────────────────────────

const MvuContext = createContext<MvuData>(mockData);

export function MvuProvider({ children }: { children: ReactNode }) {
  const [data, setData] = useState<MvuData>(mockData);

  useEffect(() => {
    const w = window as any;
    if (!w.Mvu) return;

    const refresh = () => {
      const d = readMvuData();
      if (d) setData(d);
    };

    (async () => {
      try {
        if (w.waitGlobalInitialized) await w.waitGlobalInitialized('Mvu');
        if (w.waitUntil) {
          await w.waitUntil(() =>
            w._.has(w.Mvu.getMvuData({ type: 'message', message_id: w.getCurrentMessageId() }), 'stat_data')
          );
        }
        refresh();
        if (w.eventOn && w.Mvu.events?.VARIABLE_UPDATE_ENDED) {
          w.eventOn(w.Mvu.events.VARIABLE_UPDATE_ENDED, refresh);
        }
      } catch (e) {
        console.warn('[MvuStore] MVU 连接失败，使用 mock 数据:', e);
      }
    })();
  }, []);

  return <MvuContext.Provider value={data}>{children}</MvuContext.Provider>;
}

export const useMvuData = () => useContext(MvuContext);
